package org.torusresearch.torusutils.types;

public class TorusNodePub {
    private String X;
    private String Y;
    public TorusNodePub(String _X, String _Y) {
        X = _X;
        Y = _Y;
    }

    public String getX() {
        return X;
    }

    public String getY() {
        return Y;
    }
}
